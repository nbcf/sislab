    public void excluir()
    {
         int opcao;
        Object[] botoes = {" SIM "," NÃO "};
        opcao = JOptionPane.showOptionDialog(null, "Confirma exclusão do registro: "+masterTable.getValueAt(masterTable.getSelectedRow(), 1), "PERGUNTA DO SISTEMA", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,null,botoes,botoes[0]);
        if (opcao ==  JOptionPane.YES_OPTION) {
           int[] selected = masterTable.getSelectedRows();
        List<sislab.proteomica.Segurança.PcsCadastrados> toRemove = new ArrayList<sislab.proteomica.Segurança.PcsCadastrados>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            sislab.proteomica.Segurança.PcsCadastrados p = list.get(masterTable.convertRowIndexToModel(selected[idx]));
            toRemove.add(p);
            entityManager.remove(p);
        }
        list.removeAll(toRemove);
                try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().begin();
            List<sislab.proteomica.Segurança.PcsCadastrados> merged = new ArrayList<sislab.proteomica.Segurança.PcsCadastrados>(list.size());
            for (sislab.proteomica.Segurança.PcsCadastrados p : list) {
                merged.add(entityManager.merge(p));
            }
            list.clear();
            list.addAll(merged);
        }

        atualizar();
        }

        atualizar();
    }
        
