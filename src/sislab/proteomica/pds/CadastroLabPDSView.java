    public void excluir()
    {
         int opcao;
        Object[] botoes = {" SIM "," NÃO "};
        opcao = JOptionPane.showOptionDialog(null, "Confirma exclusão do registro: "+masterTable.getValueAt(masterTable.getSelectedRow(), 1)+ "do Banco de dados ? ", "PERGUNTA DO SISTEMA", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,null,botoes,botoes[0]);
        if (opcao ==  JOptionPane.YES_OPTION) {
                   int[] selected = masterTable.getSelectedRows();
        List<sislab.proteomica.pds.Pds> toRemove = new ArrayList<sislab.proteomica.pds.Pds>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
                sislab.proteomica.pds.Pds p = list.get(masterTable.convertRowIndexToModel(selected[idx]));
            toRemove.add(p);
            entityManager.remove(p);
        }
        list.removeAll(toRemove);
              try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().begin();
            List<sislab.proteomica.pds.Pds> merged = new ArrayList<sislab.proteomica.pds.Pds>(list.size());
            for (sislab.proteomica.pds.Pds p : list) {
                merged.add(entityManager.merge(p));
            }
            list.clear();
            list.addAll(merged);
        }
        atualizar();
        }
        atualizar();
    }
        
        
    public void novo()
    {
        sislab.proteomica.pds.Pds p = new sislab.proteomica.pds.Pds();
        entityManager.persist(p);
        p.setIdLab(cont.getCadLaboratorio().getIdLab().toString());
        list.add(p);
        int row = list.size() - 1;
        masterTable.setRowSelectionInterval(row, row);
        masterTable.scrollRectToVisible(masterTable.getCellRect(row, 0, true));
        callFormulario();
    
    
    }


    public void atualizar()
    {
            entityManager.getTransaction().rollback();
        entityManager.getTransaction().begin();
        java.util.Collection data = query.getResultList();
        for (Object entity : data) {
            entityManager.refresh(entity);
        }
        list.clear();
        list.addAll(data);
                                query = entityManager.createNamedQuery("Pds.findByIdLab");
                                query.setParameter("idLab", cont.getCadLaboratorio().getIdLab().toString());
                                java.util.Collection dadosResposta1 = query.getResultList();
                                list.clear();
                                list.addAll(dadosResposta1);
        
        
        
    }
