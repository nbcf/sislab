    public void excluir()
    {
         int opcao;
        Object[] botoes = {" SIM "," NÃO "};
        opcao = JOptionPane.showOptionDialog(null, "Confirma exclusão do registro da nota: "+masterTable.getValueAt(masterTable.getSelectedRow(), 1), "PERGUNTA DO SISTEMA", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,null,botoes,botoes[0]);
        if (opcao ==  JOptionPane.YES_OPTION) {
        int[] selected = masterTable.getSelectedRows();
        List<sislab.proteomica.beans.Fornecedor> toRemove = new ArrayList<sislab.proteomica.beans.Fornecedor>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            sislab.proteomica.beans.Fornecedor f = list.get(masterTable.convertRowIndexToModel(selected[idx]));
            toRemove.add(f);
            entityManager.remove(f);
        }
        list.removeAll(toRemove);
                try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().begin();
            List<sislab.proteomica.beans.Fornecedor> merged = new ArrayList<sislab.proteomica.beans.Fornecedor>(list.size());
            for (sislab.proteomica.beans.Fornecedor f : list) {
                merged.add(entityManager.merge(f));
            }
            list.clear();
            list.addAll(merged);
        }

        atualizar();
        }

        atualizar();
    }
        
