    public void excluir()
    {
         int opcao;
        Object[] botoes = {" SIM "," NÃO "};
        opcao = JOptionPane.showOptionDialog(null, "Confirma exclusão do registro do: "+masterTable.getValueAt(masterTable.getSelectedRow(), 1), "PERGUNTA DO SISTEMA", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE,null,botoes,botoes[0]);
        if (opcao ==  JOptionPane.YES_OPTION) {
              int[] selected = masterTable.getSelectedRows();
        List< sislab.proteomica.view.estoque.NomeEstoque> toRemove = new ArrayList<sislab.proteomica.view.estoque.NomeEstoque>(selected.length);
        for (int idx = 0; idx < selected.length; idx++) {
            sislab.proteomica.view.estoque.NomeEstoque n = list.get(masterTable.convertRowIndexToModel(selected[idx]));
            toRemove.add(n);
            entityManager.remove(n);
        }
        list.removeAll(toRemove);
          try {
            entityManager.getTransaction().commit();
            entityManager.getTransaction().begin();
        } catch (RollbackException rex) {
            rex.printStackTrace();
            entityManager.getTransaction().begin();
            List<sislab.proteomica.view.estoque.NomeEstoque> merged = new ArrayList<sislab.proteomica.view.estoque.NomeEstoque>(list.size());
            for (sislab.proteomica.view.estoque.NomeEstoque n : list) {
                merged.add(entityManager.merge(n));
            }
            list.clear();
            list.addAll(merged);
        }
//        jTabbedPane1.setEnabledAt(1, false);
        atualizar();
        }
//        jTabbedPane1.setEnabledAt(1, false);
//        jTabbedPane1.setSelectedIndex(0);
        atualizar();
    }
        
        
